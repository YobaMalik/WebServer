package com.example.demo.Form;

public class TPTCForm  {
    private String aggrState;
    private double operPress;
    private double maxDn;
    private double desTemp;
    private String steel;

    public String getAggrState() {
        return aggrState;
    }

    public void setAggrState(String aggrState) {
        this.aggrState = aggrState;
    }

    public double getOperPress() {
        return operPress;
    }

    public void setOperPress(double operPress) {
        this.operPress = operPress;
    }

    public double getMaxDn() {
        return maxDn;
    }

    public void setMaxDn(double maxDn) {
        this.maxDn = maxDn;
    }

    public double getDesTemp() {
        return desTemp;
    }

    public void setDesTemp(double desTemp) {
        this.desTemp = desTemp;
    }

    public String getSteel() {
        return steel;
    }

    public void setSteel(String steel) {
        this.steel = steel;
    }
}
