package com.example.demo.TensionCalcASME;

public class SpecForm {
    private String spec;
    private String grade;
    private double temp;
    private double outTensiton;

    public String getSpec() {
        return spec;
    }

    public void setSpec(String fullName) {
        this.spec = fullName;
    }

    public String getGrade() {
        return grade;
    }

    public void setGrade(String grade) {
        this.grade = grade;
    }

    public double getTemp() {
        return temp;
    }

    public void setTemp(double temp) {
        this.temp = temp;
    }

    public double getOutTensiton() {
        return outTensiton;
    }

    public void setOutTensiton(double outTensiton) {
        this.outTensiton = outTensiton;
    }
}
